		<h4 class="radius secondary label">State of Illinois Probate Court</h4>

	<p>The State of Illinois has increased the minimum amount of property, held in your name alone subject to the Probate jurisdiction, from $50,000 to $100,000. This means that upon your death, all assets held in your name alone that total over $100,000 must be subjected to the Probate court process. This process wastes time, money and energy for the Nominated Executor of your Estate. Save your loved ones a nuisance by creating a Living Trust, and by transferring your assets into that Trust. Assets in your name alone, under $100,000, may now be transferred by Small Estate Affidavit, prepared 30 days after the death of the decedent. Any estates that are transferred by affidavit are not subject to jurisdiction by the Probate Court. The estate totaling less than $100,000, however, will still be subject to creditors’ claims for two (2) years after transfer by affidavit. </p>
	<p>Some assets you own are outside of the Probate Court’s jurisdiction, but you still want to make sure the beneficiaries of these assets are designated correctly. These assets include: 401K’s or other retirement plans, IRA plans, Life insurance, and some savings accounts or CDs. In order to make the transition smoother after your death, make sure the beneficiaries for these assets are 1) correctly designated the way you would like them, and 2) clearly designated on all documents, to avoid confusion and resentment from your descendants. In addition to clarifying the beneficiary designation, you may want to investigate the tax consequences of beneficiaries withdrawing the principal of these plans after your death. One option many companies offer is to enable the spouse to roll-over your 401K into his or her own IRA. This will prevent the spouse from being taxed on the full amount on their individual income tax for the year of withdrawal.</p>
	

	<h4 class="radius secondary label">Federal Estate Tax Exemption 2013</h4>

	<p>The Estate Tax is a tax on your right to transfer property at your death. 
		It consists of an accounting of everything you own or have certain interests 
		in at the date of death (Refer to Form 706 (PDF)). The fair market value of these 
		items is used, not necessarily what you paid for them or what their values were
		 when you acquired them. The total of all of these items is your "Gross Estate." 
		 The includible property may consist of cash and securities, real estate, insurance, 
		 trusts, annuities, business interests and other assets. <p/>

<p/>Once you have accounted for the Gross Estate, certain deductions (and in special circumstances, 
reductions to value) are allowed in arriving at your "Taxable Estate." 
These deductions may include mortgages and other debts, estate administration expenses, property that passes to surviving spouses and qualified charities. The value of some operating business interests or farms may be reduced for estates that qualify.

After the net amount is computed, the value of lifetime taxable gifts 
(beginning with gifts made in 1977) is added to this number and the tax is computed. 
The tax is then reduced by the available unified credit.<p/>

<p/>Most relatively simple estates (cash, publicly traded securities, 
small amounts of other easily valued assets, and no special deductions or elections, 
or jointly held property) do not require the filing of an estate tax return. 
A filing is required for estates with combined gross assets and prior taxable 
gifts exceeding $1,500,000 in 2004 - 2005; $2,000,000 in 2006 - 2008; $3,500,000 
for decedents dying in 2009; and $5,000,000 or more for decedent's dying in 2010 and 
2011 (note: there are special rules for decedents dying in 2010); $5,120,000 in 2012 
and $5,250,000 in 2013.
	</p>


	
