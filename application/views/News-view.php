	<h4>Federal Estate Tax Exemption</h4>

	<p>The current Federal Estate Tax exemption (reflected as a credit equivalent) has increased to 
		$2.0 million dollars, for decedents dying after January 1, 2006. This may affect the allocation 
		between marital (Trust A) and by-pass trusts (Trust B or Credit shelter), depending upon the 
		Trust language used in the underlying trust document. This may also affect the amount qualifying 
		for the marital deduction and available for the surviving spouse. The exemption will increase to
		$3.5 million for decedents dying after January 1, 2009. Check back for more updates as Congress 
		debates this important estate planning regulation.
	</p>
	<hr/>

	<h3>Health Insurance Portability and Accountability Act of 1996 </h3>

	<p>A new law was enacted recently called the Health Insurance Portability and Accountability Act of 
		1996 (aka HIPAA), 42 USC 1320d and 45 CFR 160-164. A provision, which we attach at the end of the 
		Power of Attorney for Health Care, enables your Agent to access all of your medical records after 
		you have become incapacitated. This is a necessity if you expect your Agent to act in your best 
		interest after you have been deemed incapable of making your own decisions. 
		How can the Agent make the best decision if they don’t have access to your medical history? 
		I strongly encourage you to contact our law firm regarding this new law, if you have not already 
		done so, and either add it to your existing Power of Attorney, or create a new, 
		updated Power of Attorney.
	</p>
