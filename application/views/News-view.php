		1996 (aka HIPAA), 42 USC 1320d and 45 CFR 160-164. A provision, which we attach at the end of the 
		Power of Attorney for Health Care, enables your Agent to access all of your medical records after 
		you have become incapacitated. This is a necessity if you expect your Agent to act in your best 
		interest after you have been deemed incapable of making your own decisions. 
		How can the Agent make the best decision if they don’t have access to your medical history? 
		I strongly encourage you to contact our law firm regarding this new law, if you have not already 
		done so, and either add it to your existing Power of Attorney, or create a new, 
		updated Power of Attorney.
	</p>

	<h3>State of Illinois Probate Court</h3>

	<p>The State of Illinois has increased the minimum amount of property, held in your name alone subject to the Probate jurisdiction, from $50,000 to $100,000. This means that upon your death, all assets held in your name alone that total over $100,000 must be subjected to the Probate court process. This process wastes time, money and energy for the Nominated Executor of your Estate. Save your loved ones a nuisance by creating a Living Trust, and by transferring your assets into that Trust. Assets in your name alone, under $100,000, may now be transferred by Small Estate Affidavit, prepared 30 days after the death of the decedent. Any estates that are transferred by affidavit are not subject to jurisdiction by the Probate Court. The estate totaling less than $100,000, however, will still be subject to creditors’ claims for two (2) years after transfer by affidavit. </p>
	<p>Some assets you own are outside of the Probate Court’s jurisdiction, but you still want to make sure the beneficiaries of these assets are designated correctly. These assets include: 401K’s or other retirement plans, IRA plans, Life insurance, and some savings accounts or CDs. In order to make the transition smoother after your death, make sure the beneficiaries for these assets are 1) correctly designated the way you would like them, and 2) clearly designated on all documents, to avoid confusion and resentment from your descendants. In addition to clarifying the beneficiary designation, you may want to investigate the tax consequences of beneficiaries withdrawing the principal of these plans after your death. One option many companies offer is to enable the spouse to roll-over your 401K into his or her own IRA. This will prevent the spouse from being taxed on the full amount on their individual income tax for the year of withdrawal.</p>




	<h3>Estate and Gift Tax: An Overview</h3>

	<p>Estate and gift taxes are imposed by the federal government on the transfer 
		of property from person to another, either at death (estate tax) or while 
		the giver of the property is still alive (gift tax).
	</p>

	<h3>Estate Tax</h3>

    <p>Estates are required to file a federal estate tax return if the value of the 
    	"gross" estate, minus certain deductions, is above a certain dollar amount 
    	(for 2011, the amount is $5 million). The gross estate includes the value of 
    	all property in which the decedent had an interest at the time of his or her 
    	death -- including such items as real estate, stocks and bonds, mortgages, 
    	notes and cash, insurance on the decedent's life, and jointly owned property. 
    	If spouses own property in joint tenancy, and one dies, one half of the value 
    	of the jointly held property is included in the gross estate of the deceased spouse.
    </p>

    <h3>Estate Tax Exemptions</h3>

	<p>Personal Exemption. Th e "personal estate tax exemption" allows a certain amount (or all) of a deceased person's estate to transfer free of the estate tax. The personal exemption is currently $5 million. Keep in mind that Congress may take further steps to change the personal estate tax exemption at any time, or even repeal the estate tax altogether, so be sure to check the regulations currently in place.</p>

	<p>Marital Deduction. A deceased person's estate can pass tax free to a surviving spouse, as long as the surviving spouse is a U.S. citizen and the deceased spouses interest in the estate passes to the surviving spouse outright (in other words, the property transfers directly to the spouse upon the decendents death).</p>

	<p>To understand how the marital deduction and personal estate tax reduction might work in practice, suppose Bob dies leaving an estate worth $12 million. Putting aside community property issues for the moment, assume that his will states that $6 million go to his wife, and $6 million to his daughter Kate. In this situation, no part of the $6 million Bob leaves to his wife will be subject to the estate tax (as long as Bob's wife is a U.S. citizen and all the property included in the $6 million passes to her outright). Of the $6 million Bob leaves to Kate, however, $1 million will be subject to the estate tax (the $6 million minus the first $5 million that is tax-free under the 2011 personal estate tax exemption).</p>

	<p>Other deductions. Other deductions against the gross estate include certain administrative expenses, funeral expenses, claims against the estate, certain taxes and other indebtedness and charitable bequests.</p>

	<h3>Filing the Estate Tax Return</h3>

	<p>The executor, personal representative, or person in possession of the estate's assets must file the estate tax return within nine months of the decedent's death. The estate can apply for a six-month extension of time to file, but the taxes must be paid within nine months of the decedent's death. The time for payment of the estate tax may be extended in certain circumstances.</p>
	<h3>State Estate Taxes</h3>

	<p>Some states also impose estate taxes. The state in which the decedent lived may impose an estate tax, and states where real estate or personal property is located may also impose an estate tax. The law of each state having any connection to the property in question must be consulted in order to assess any tax consequences associated with the property transfer. A skilled estate planning or taxation attorney can help make such a determination.</p>
	

	<h3>Gift Tax</h3>

	<p>The gift tax is a tax on the legal transfer of property from one person to another, during the giving person's l lifetime. Certain gifts are exempt from the gift tax. These include:</p>

    <ul class="disc">Gifts valued at a dollar amount of $13,000 or less to any one individual in a single calendar year;
        <li>Gifts to a spouse;</li>
        <li>Payment of tuition or medical expenses on someone elses behalf;</li>
        <li>Charitable contributions; and</li>
        <li>Certain gifts to political organizations.</li>
    </ul>

	<h3>The Unified Gift Tax Credit</h3>

	<p>A credit, in terms of the tax code, reduces the amount of tax that you have to pay. If large enough, the credit could eliminate the required tax altogether. In the case of gift taxes, the Unified Credit applies. The Unified Credit is a lifetime credit of $345,800, and taxpayers must apply it to all taxable gifts in a year. Each application will reduce the amount of the credit available for use in a later year.</p>

	<p>To illustrate, say you give your two sons $25,000 each this year. The first $13,000 of the gifts are exempt from the gift tax, which leaves $24,000 in taxable gifts. The gift tax on $24,000 comes to $4,680. After applying the Unified Credit, however, that tax disappears. Assuming that you have not given taxable gifts during any other year, the Unified Credit left available to you will drop from $345,800 to $341,120, which is the amount that you can apply to any future taxable gifts.</p>
