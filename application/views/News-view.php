	<h3>State of Illinois Probate Court</h3>

	<p>The State of Illinois has increased the minimum amount of property, held in your name alone subject to the Probate jurisdiction, from $50,000 to $100,000. This means that upon your death, all assets held in your name alone that total over $100,000 must be subjected to the Probate court process. This process wastes time, money and energy for the Nominated Executor of your Estate. Save your loved ones a nuisance by creating a Living Trust, and by transferring your assets into that Trust. Assets in your name alone, under $100,000, may now be transferred by Small Estate Affidavit, prepared 30 days after the death of the decedent. Any estates that are transferred by affidavit are not subject to jurisdiction by the Probate Court. The estate totaling less than $100,000, however, will still be subject to creditors’ claims for two (2) years after transfer by affidavit. </p>
	<p>Some assets you own are outside of the Probate Court’s jurisdiction, but you still want to make sure the beneficiaries of these assets are designated correctly. These assets include: 401K’s or other retirement plans, IRA plans, Life insurance, and some savings accounts or CDs. In order to make the transition smoother after your death, make sure the beneficiaries for these assets are 1) correctly designated the way you would like them, and 2) clearly designated on all documents, to avoid confusion and resentment from your descendants. In addition to clarifying the beneficiary designation, you may want to investigate the tax consequences of beneficiaries withdrawing the principal of these plans after your death. One option many companies offer is to enable the spouse to roll-over your 401K into his or her own IRA. This will prevent the spouse from being taxed on the full amount on their individual income tax for the year of withdrawal.</p>
	

